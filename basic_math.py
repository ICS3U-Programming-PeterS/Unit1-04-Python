def main():
    print("7-3={}". format(7-3))
    print("2+4={}". format(2+4))
    print("9*5={}". format(9*5))
    print("10/6={}". format(10/6))
    print("3**4={}". format(3**4))
    print("2+5*3={}". format(2+5*3)) # exponent
    print("81**0.5={}". format(81**0.5)) # square root

if __name__ == "__main__":
    main()
